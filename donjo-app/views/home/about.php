1. Offline
<b><br /></b>
Aplikasi diinstall dalam komputer server di kantor desa dan dioperasikan sebagai server (pusat data) yang bersifat lokal. Karena tidak terhubung ke internet,  offline hanya bisa diakses dalam jaringan lokal. Sistem offline ini direkomendasikan untuk diterapkan dalam penggunaan aplikasi  harian. Database dari hasil proses olah data secara offline itu dapat diunggah ke sistem online secara berkala.
<b><br /></b>
<b><br /></b>
2. Online
<b><br /></b>
 akan optimal jika terhubung ke internet sebagai sistem online berbasis web.  online akan otomatis berfungsi juga sebagai website desa. Website desa ini memiliki fungsi yang terbagi dalam dua bagian, yakni bagian depan yang bisa diakses oleh publik dan bagian dalam yang hanya bisa diakses oleh administrator sistem.<br />
<b><br /></b>
<br />
<b>MANAJEMEN AKSES </b><br />
<br />
Aplikasi  dirancang sejak awal oleh CRI untuk mengelola data dasar desa dan informasi desa. Data dasar yang dikelola meliputi data dasar kependudukan dan data dasar aset/sumber daya desa. Data dasar ini menjadi tanggung jawab pemerintah desa dalam pengelolaannya. Hanya pengguna (user) dari pemerintah desa dan tim yang dikoordinasikan oleh pemerintah desa saja yang akan memiliki kewenangan dan hak akses ke dalam sistem. Sementara, user di luar pemerintah desa hanya akan memiliki akses terbatas pada fungsi olah informasi untuk website desa.<br />
<br />
Tingkat user (pengguna) dalam :<br />

1. Administrator : adalah orang/tim yang bertanggung jawab penuh atas olah data dan informasi dalam  dan website desa. Orang/tim ini ditunjuk oleh pemerintah desa disahkan dengan surat keputusan kepala desa.<br />
- Peran olah data : entry, edit, delete data dasar<br />
- Peran olah informasi : tulis, edit, publish artikel website<br />

2. Operator: adalah orang/tim yang bertugas membantu administrator mengelola data dan informasi, tetapi dengan kewenangan yang lebih terbatas.<br />
- Peran olah data : entry, edit data dasar<br />
- Peran olah informasi : tulis dan input modul (xls dan pdf)<br />
- Wajib melakukan verfikasi ID (Masa berlaku 1 tahun) <br />

3. Kepala Desa: adalah orang/tim yang bertugas sebagai redaksi media website desa dan hanya dapat melakukan olah informasi berupa artikel website.<br />
- Peran olah informasi : tulis, edit artikel  <br />
- Verifikasi Data Operator<br />
